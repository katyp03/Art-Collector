function styleModalShow(){
  document.getElementById('styleModal').style.display = "block";
}
function mediumModalShow(){
  document.getElementById('mediumModal').style.display = "block";
}
function dateModalShow(){
  document.getElementById('dateModal').style.display = "block";
}
function sizeModalShow(){
  document.getElementById('sizeModal').style.display = "block";
}
function closeModal() {
  document.getElementById('styleModal').style.display = "none";
  document.getElementById('mediumModal').style.display = "none";
  document.getElementById('dateModal').style.display = "none";
  document.getElementById('sizeModal').style.display = "none";
}
